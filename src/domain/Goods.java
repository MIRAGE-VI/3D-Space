package domain;

public class Goods {
	   private int G_id;                 
	   private String G_name;             
	   private String G_price;              
	   private String G_material;           
	   private String G_color;             
	   private int G_num;              
	   private int G_shoppingNum;
	public int getG_id() {
		return G_id;
	}
	public void setG_id(int g_id) {
		G_id = g_id;
	}
	public String getG_name() {
		return G_name;
	}
	public void setG_name(String g_name) {
		G_name = g_name;
	}
	public String getG_price() {
		return G_price;
	}
	public void setG_price(String g_price) {
		G_price = g_price;
	}
	public String getG_material() {
		return G_material;
	}
	public void setG_material(String g_material) {
		G_material = g_material;
	}
	public String getG_color() {
		return G_color;
	}
	public void setG_color(String g_color) {
		G_color = g_color;
	}
	public int getG_num() {
		return G_num;
	}
	public void setG_num(int g_num) {
		G_num = g_num;
	}
	public int getG_shoppingNum() {
		return G_shoppingNum;
	}
	public void setG_shoppingNum(int g_shoppingNum) {
		G_shoppingNum = g_shoppingNum;
	}
}
